package co.edu.unbosque.model;

/**
 * The type Par.
 */
public class Par {
	
	private int x;
	private int y;

	/**
	 * Gets x.
	 *
	 * @return the x
	 */
	public int getX() {
		return x;
	}

	/**
	 * Sets x.
	 *
	 * @param x the x
	 */
	public void setX(int x) {
		this.x = x;
	}

	/**
	 * Gets y.
	 *
	 * @return the y
	 */
	public int getY() {
		return y;
	}

	/**
	 * Sets y.
	 *
	 * @param y the y
	 */
	public void setY(int y) {
		this.y = y;
	}

	
	

}
