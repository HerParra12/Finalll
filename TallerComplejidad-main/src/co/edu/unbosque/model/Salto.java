package co.edu.unbosque.model;

/**
 * The type Salto.
 */
public class Salto {
	
	private Par origen;
	private Par destine;
	private Par comida;

	/**
	 * Gets origen.
	 *
	 * @return the origen
	 */
	public Par getOrigen() {
		return origen;
	}

	/**
	 * Sets origen.
	 *
	 * @param origen the origen
	 */
	public void setOrigen(Par origen) {
		this.origen = origen;
	}

	/**
	 * Gets destine.
	 *
	 * @return the destine
	 */
	public Par getDestine() {
		return destine;
	}

	/**
	 * Sets destine.
	 *
	 * @param destine the destine
	 */
	public void setDestine(Par destine) {
		this.destine = destine;
	}

	/**
	 * Gets comida.
	 *
	 * @return the comida
	 */
	public Par getComida() {
		return comida;
	}

	/**
	 * Sets comida.
	 *
	 * @param comida the comida
	 */
	public void setComida(Par comida) {
		this.comida = comida;
	}

}
